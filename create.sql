\c geek_collaborators;

CREATE TABLE fullstack_collaborators (
id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
name varchar,
language varchar,
contact int,
role varchar
);

CREATE TABLE fullstack (
id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
name varchar,
 owner varchar,
 owner_contact varchar
 );

CREATE TABLE contacts(
    id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR,
    email VARCHAR,
    subject VARCHAR,
    message VARCHAR
);

